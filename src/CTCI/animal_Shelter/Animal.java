public class Animal {


    private String name;
    private int age;
    private int type;
    private int arrival_num;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

    public int getArrival_num() {
        return arrival_num;
    }

    public void setArrival_num(int arrival_num) {
        this.arrival_num = arrival_num;
    }

}
